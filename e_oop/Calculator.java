package e_oop;

public class Calculator {
	long plus(long a, long b){
		return a + b;
	}
	
	long minus(long a, long b){
		return a - b;
	}
	
	long multiply(long a, long b){
		return a * b;
	}
	
	long division(long a, long b){
		return a / b;
	}
	
	long remainder(long a, long b){
		return a % b;
	}
}
